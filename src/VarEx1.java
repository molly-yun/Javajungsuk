public class VarEx1 {
    public static void main(String[] args) {
        int year = 0;   //변수 선언과 동시에 초기화
        int age = 14;

        System.out.println(year);
        System.out.println(age);

        year = age + 2000;
        age = age + 1;

        System.out.println(year);
        System.out.println(age);
    }
}
